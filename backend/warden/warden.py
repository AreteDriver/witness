"""Warden — autonomous threat hypothesis loop.

Ingests events → generates hypotheses → evaluates against doctrine →
commits or discards → audits. Runs as background task alongside poller.

NEVER CRASHES. All errors logged and continued.
"""

import json
import time
from dataclasses import dataclass, field
from pathlib import Path

from backend.core.config import settings
from backend.core.logger import get_logger
from backend.db.database import get_db

logger = get_logger("warden")

AUDIT_PATH = Path("data/warden_audit.jsonl")

# Doctrine weights for hypothesis scoring
WEIGHTS = {
    "evidence": 0.35,
    "recency": 0.25,
    "impact": 0.25,
    "novelty": 0.15,
}

COMMIT_THRESHOLD = 0.5
OPERATOR_ALERT_THRESHOLD = 0.8


@dataclass
class Hypothesis:
    """A single threat hypothesis generated by the Warden."""

    category: str  # THREAT, LOGISTICS, INTEL
    title: str
    description: str
    evidence_count: int = 0
    evidence_summary: str = ""
    scores: dict[str, float] = field(default_factory=dict)
    composite: float = 0.0
    committed: bool = False
    zone_id: str = ""
    entity_id: str = ""

    def score(self, evidence: float, recency: float, impact: float, novelty: float) -> float:
        """Score hypothesis against doctrine weights. Returns composite 0.0-1.0."""
        self.scores = {
            "evidence": min(1.0, max(0.0, evidence)),
            "recency": min(1.0, max(0.0, recency)),
            "impact": min(1.0, max(0.0, impact)),
            "novelty": min(1.0, max(0.0, novelty)),
        }
        self.composite = sum(self.scores[k] * WEIGHTS[k] for k in WEIGHTS)
        return self.composite

    def to_dict(self) -> dict:
        return {
            "category": self.category,
            "title": self.title,
            "description": self.description,
            "evidence_count": self.evidence_count,
            "evidence_summary": self.evidence_summary,
            "scores": self.scores,
            "composite": round(self.composite, 3),
            "committed": self.committed,
            "zone_id": self.zone_id,
            "entity_id": self.entity_id,
        }


def _audit_log(hypothesis: Hypothesis, action: str) -> None:
    """Append-only audit log. Sacred — never truncated."""
    AUDIT_PATH.parent.mkdir(parents=True, exist_ok=True)
    entry = {
        "timestamp": int(time.time()),
        "action": action,
        "hypothesis": hypothesis.to_dict(),
    }
    try:
        with open(AUDIT_PATH, "a") as f:
            f.write(json.dumps(entry) + "\n")
    except Exception as e:
        logger.error("Audit log write failed: %s", e)


async def _notify_operator(hypothesis: Hypothesis) -> None:
    """Fire Discord webhook for high-confidence THREAT findings."""
    webhook_url = settings.DISCORD_WEBHOOK_URL
    if not webhook_url:
        return

    try:
        from backend.analysis.oracle import EVE_ORANGE, EVE_RED, fire_webhook

        color = EVE_RED if hypothesis.category == "THREAT" else EVE_ORANGE
        await fire_webhook(
            webhook_url,
            f"WARDEN: {hypothesis.title}",
            (
                f"{hypothesis.description}\n\n"
                f"**Evidence**: {hypothesis.evidence_summary}\n"
                f"**Confidence**: {hypothesis.composite:.0%}"
            ),
            color,
        )
    except Exception as e:
        logger.error("Operator notification failed: %s", e)


